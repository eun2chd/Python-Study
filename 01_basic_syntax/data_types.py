# 주요 데이터 타입 : int, float, str, bool
#  컬렉션 데이터 타입 : list, tuple, dict, set

# 주요 데이터 타입
integer = 10 #정수형
floating = 3.13 #실수형
string = "Hello"
boolean = False

# 리스트 (list)
fruits = ["apple", "banana", "cherry"]
print(fruits[0]) # 출력 apple

# 튜플 (tuple)
coordinates = (4,5)
print(coordinates) # 출력 (4,5)

# 딕셔너리 (dict)
person = {"name" : "Alice", "age" : 30}
print(person["age"]) # 출력 Alice

# 집합(set)
unique_numbers = {1,2,3,4}
print(unique_numbers) # 출력 : {1,2,3,4}
