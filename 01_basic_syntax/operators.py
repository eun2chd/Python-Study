# 연산자
# 산술연산자 : +, -, *, / , // , % , **
# 비교연산자 : ==, !=, <, >, <=, >=
# 논리연산자 : and, or, not

a, b = 10, 3
print(a + b) # 출력 13
print(a // b) # 출력 3 (정수 나누기)
print(a ** b) # 1000 지수 연산

# 비교 연산자
print(a > b) # 출력 True
print(a == 10) # 출력 True

# 논리 연산자
is_raining = False
is_sunny = True
print(is_raining or is_sunny) # 출력 : True

